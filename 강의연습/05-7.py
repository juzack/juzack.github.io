''' 외부 라이브러리
pip 파이썬 모듈이나 패키지를 쉽게 설치할 수있도록 도와주는 도구, 외부 라이브러리를 사용하려면 pip 도구를 먼저 설치
    PyPI(python package index) 파이썬 소프트웨어가 모인 저장 공간, 현재 10만 건이상의 파이썬 패키지가 등록되어 있으며 누구나 내려받아 사용 가능

    pip install SomePackage
    pip uninstall SomePackage
    pip install SomePackage == 1.1
    pip install --upgrade SomePackage
    pip list

'''